/*
------------------------------------------------------------------------------------------
대입연산자 (=)
------------------------------------------------------------------------------------------
변수에 값을 할당할 때 사용
------------------------------------------------------------------------------------------
*/
let a = 1;
const b = "1";
console.log(a);
console.log(b);

console.log("---------------------------------");

/*
------------------------------------------------------------------------------------------
비교 연산자 (==, !=)
------------------------------------------------------------------------------------------
피연산자의 값이 같은지 비교
타입이 달라도 괜찮음 -> 값만 비교하기 때문
------------------------------------------------------------------------------------------
동등 연산자 (=== , !==)
------------------------------------------------------------------------------------------
피연산자의 값과 타입을 모두 비교
엄격한 동등 연산자
------------------------------------------------------------------------------------------
*/
console.log(1 == 1); //참 (1), True
console.log(1 == 2); //거짓 (0), False
console.log(1 == "1"); // 참(1), True -> 타입비교 x
console.log(1 != 1); //거짓 (0), False -> 숫자1과 숫자1은 같기 때문
console.log(1 != 2); //참 (1), True
console.log(1 != "1"); //거짓 (0), False

console.log("---------------------------------");

console.log(1 === 1); //참 (1), True
console.log(1 === 2); //거짓 (0), False
console.log(1 === "1"); //거짓 (0), False -> 타입까지비교
console.log(1 !== 1); //거짓 (0),False -> 숫자1과 숫자1은 같기 때문
console.log(1 !== 2); //참 (1), True
console.log(1 !== "1"); //참 (1), True -> 타입까지비교

console.log("---------------------------------");
/*
------------------------------------------------------------------------------------------
비교연산자
------------------------------------------------------------------------------------------
> , >=, <, <=
------------------------------------------------------------------------------------------
*/
console.log(2 > 1); //참 (1), True
console.log(1 >= 1); //참 (1), True
console.log(2 < 1); //거짓 (0), False
console.log(1 <= 1); //참 (1), True

console.log("---------------------------------");

/*
------------------------------------------------------------------------------------------
산술연산자
------------------------------------------------------------------------------------------
+, -, *, /, %(나머지), **(거듭제곱)
------------------------------------------------------------------------------------------
*/
console.log(1 + 2); //3
console.log(1 - 2); //-1
console.log(1 * 2); //2
console.log(1 / 2); //0.5
//나머지 연산자
console.log(1 % 2); //1
console.log(8 % 3); //2
console.log(8 % 5); //3
console.log(1 ** 2); //1

console.log("---------------------------------");

/*
------------------------------------------------------------------------------------------
논리연산자
------------------------------------------------------------------------------------------
!, && , ||
------------------------------------------------------------------------------------------
*/
console.log(!true); // false
console.log(!false); // true
console.log(!!true); // true (거의 안씀, 두번뒤집음==결국 원본)
console.log(!!false); // false
console.log(true && true); //true
console.log(true && false); //false
console.log(1 < 2 && 1 < 5); //true
console.log(1 > 2 && 1 < 5); //false
console.log(true || true); //true
console.log(true || false); //true

//연산자 응용
console.log(!(2 > 1)); //false
console.log(2 > 1 && -2 < 1); //true
console.log((2 > 1 && -2 < 1) || 5 > 3); //true

console.log(3 <= 1);
