/*
typeof 자료형 확인
------------------------------------------------------------------------------------------
해당 자료형이 어떤것인지 확인 가능
------------------------------------------------------------------------------------------
*/

console.log(typeof "문자열"); //string
console.log(typeof 234); //number
console.log(typeof {}); //object
console.log(typeof []); //object
console.log(typeof NaN); // Nan : Not a Number -> number
let und;
console.log(typeof und); // undefined










